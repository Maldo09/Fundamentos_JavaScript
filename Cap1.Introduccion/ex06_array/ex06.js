/* ARRAYS O ARREGLOS
Un array es una estructura de datos que se utiliza para almacenar una colección ordenada de elementos.
Estos elementos pueden ser cadenas de texto, valores numericos como: enteros, decimales; valores booleanos, entre otros. Pueden ser tambien un conjunto de funciones e inclusive otros arrays.
*/

let frutas = ["piña", "Maracuya", "Banano", "Fresa", "Mango"]; // Array de cadenas de texto representando frutas

console.log(frutas);
console.log(frutas[3]);
console.log(frutas [0]);
console.log(frutas [4]);
console.log(frutas [1]);
console.log(frutas [2]);
